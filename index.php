         <p class='indent padding10'>
    The <span class='bold'>Affinity Advisory Network</span> was constituted over two decades ago by founder Robert Hall in Canton, OH. At the time, Robert enjoyed a career as one of the top independent producers in the country, and as a result often mentored and trained other advisors striving to reach that same pinnacle. From these early interactions morphed the concept of a better, more inclusive marketing organization that put advisors in a position to do what they do best, all while alleviating some of the more cumbersome, non-revenue generating tasks from their plates.</p>
         
         

    <p class='indent padding10'>
Robert’s goal was to institute an atmosphere of growth for the independent producer, regardless of whether they are a one-man shop or an organization of hundreds of advisors. The outcome is a synergy that provides value, observation and guidance to all advisors looking to grow their respective businesses.</p>
        
